package apurba;

public class ClassOfDemoTwo {
	private String name;
	private int number;
	private double value; 
	
	
	public ClassOfDemoTwo(String name, int number, double value) {
		this.name = name;
		this.number = number;
		this.value = value;
	}
	
	
	public String getName() {
		return name;
	}
	
	public int getNumber() {
		return number;
	}
	
	public double getValue() {
		return value;
	}
}
